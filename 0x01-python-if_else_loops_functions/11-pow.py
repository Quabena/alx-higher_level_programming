#!/usr/bin/python3

def pow(a, b):
    """
    Computes a to the power of b and returns the value.

    Args:
        a (int): The base integer.
        b (int): The exponent integer.

    Returns:
        int: The result of a raised to the power of b.
    """
    result = a ** b  # Compute a raised to the power of b

    return result
